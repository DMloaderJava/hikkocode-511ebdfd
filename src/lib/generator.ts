// Simulated AI generation - produces a React project structure
export async function generateProject(
  prompt: string,
  existingFiles: GeneratedFile[],
  onLoadingMessage: (msg: string) => void
): Promise<GeneratedFile[]> {
  const { funnyLoadingMessages } = await import("@/context/AppContext");

  // Cycle through funny loading messages
  let msgIndex = 0;
  const interval = setInterval(() => {
    onLoadingMessage(funnyLoadingMessages[msgIndex % funnyLoadingMessages.length]);
    msgIndex++;
  }, 1800);

  // Simulate generation delay
  await new Promise(r => setTimeout(r, 3000 + Math.random() * 2000));
  clearInterval(interval);

  const lowerPrompt = prompt.toLowerCase();

  // Generate different templates based on prompt keywords
  if (lowerPrompt.includes("login") || lowerPrompt.includes("auth")) {
    return generateAuthTemplate(prompt);
  }
  if (lowerPrompt.includes("dashboard") || lowerPrompt.includes("admin")) {
    return generateDashboardTemplate(prompt);
  }
  if (lowerPrompt.includes("todo") || lowerPrompt.includes("task")) {
    return generateTodoTemplate(prompt);
  }
  if (lowerPrompt.includes("landing") || lowerPrompt.includes("portfolio")) {
    return generateLandingTemplate(prompt);
  }
  if (lowerPrompt.includes("e-commerce") || lowerPrompt.includes("shop") || lowerPrompt.includes("store")) {
    return generateEcommerceTemplate(prompt);
  }

  // Default: generate based on existing files or create new
  if (existingFiles.length > 0) {
    return generateIterativeUpdate(prompt, existingFiles);
  }

  return generateDefaultTemplate(prompt);
}

