function generateIterativeUpdate(prompt: string, existingFiles: GeneratedFile[]): GeneratedFile[] {
  // For iterative updates, modify existing files with a comment
  return existingFiles.map(file => {
    if (file.language === "html") {
      return {
        ...file,
        content: file.content.replace(
          "</body>",
          `  <!-- Updated based on: "${prompt.slice(0, 60)}" -->\n</body>`
        ),
      };
    }
    if (file.language === "css") {
      return {
        ...file,
        content: file.content + `\n\n/* Update: ${prompt.slice(0, 60)} */\n.updated { color: #00ff88; }\n`,
      };
    }
    return file;
  });
}
